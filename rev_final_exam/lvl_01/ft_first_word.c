#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	if (argc == 2)
	{
		while (argv[i] == ' ' || argv[i] == '\t')
			i++;
		while (argv[i] != '\0' && argv[i] != ' ' && argv[i] != '\t')
		{
			write(1, &argv[i], 1);
			i++;
		}
		write(1, "\n", 1);
		return (0);
	}
}
